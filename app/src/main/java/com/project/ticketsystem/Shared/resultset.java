    public void setCpassenger(String cpassenger) {
        this.cpassenger = cpassenger;
    }

    public String getCform() {
        return cform;
    }

    public void setCform(String cform) {
        this.cform = cform;
    }

    public String getCto() {
        return cto;
    }

    public void setCto(String cto) {
        this.cto = cto;
    }

    public String getCperson() {
        return cperson;
    }

    public void setCperson(String cperson) {
        this.cperson = cperson;
    }

    public String getCdate() {
        return cdate;
    }

    public void setCdate(String cdate) {
        this.cdate = cdate;
    }

    public String getCcurrentdate() {
        return ccurrentdate;
    }

    public void setCcurrentdate(String ccurrentdate) {
        this.ccurrentdate = ccurrentdate;
    }

    public String getCcurrenttime() {
        return ccurrenttime;
    }

    public void setCcurrenttime(String ccurrenttime) {
        this.ccurrenttime = ccurrenttime;
    }
